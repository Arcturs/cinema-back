CREATE TABLE IF NOT EXISTS ticket
(
    ticket_id  BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    seat_id    BIGINT REFERENCES seat (seat_id) ON DELETE CASCADE       NOT NULL,
    session_id BIGINT REFERENCES session (session_id) ON DELETE CASCADE NOT NULL,
    user_id    BIGINT REFERENCES user_info (user_id) ON DELETE CASCADE  NOT NULL
);